///////////////////////////////////
// IMPORT SCHEMA AND MODEL
///////////////////////////////////
const { Schema, model } = require("mongoose");

////////////////////////////////////
// CREATE SCHEMA
////////////////////////////////////

const fgmSchema = new Schema(
  {
    username: { type: String, required: true },
    fgm: String,
  },
  { timestamps: true }
);

////////////////////////////////////
// CREATE MODEL
////////////////////////////////////
const Fgm = model("fgm", fgmSchema);

////////////////////////////////////
// EXPORT MODEL
////////////////////////////////////
module.exports = Fgm;
