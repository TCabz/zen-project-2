///////////////////////////////////
// IMPORT SCHEMA AND MODEL
///////////////////////////////////
const { Schema, model } = require("mongoose");

////////////////////////////////////
// CREATE SCHEMA
////////////////////////////////////

const fgmSchema = new Schema({
  username: { type: String, required: true, unique: true },
  fgm: { type: String, required: true },
});

////////////////////////////////////
// CREATE MODEL
////////////////////////////////////
const Fgm = model("fgm", fgmSchema);

////////////////////////////////////
// EXPORT MODEL
////////////////////////////////////
module.exports = Fgm;
